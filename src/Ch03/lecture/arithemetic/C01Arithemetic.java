package Ch03.lecture.arithemetic;

public class C01Arithemetic {
    public static void main(String[] args) {
        int a = 3;
        int b = 9;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b); //b를 a로 나눈 나머지
    }
}
